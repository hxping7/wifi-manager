package main.java.com.mundosica.wifi.manager.Model;

import java.util.ArrayList;
import java.util.List;

/**
 * 
 * @author @Fitorec <chanerec at gmail.com>
 */
abstract class NetworkAbstract {
    protected String name;
    protected String type;
    protected String auth;
    protected String encryption;
    protected List<BSSID> BSSIDS = new ArrayList<>();

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the auth
     */
    public String getAuth() {
        return auth;
    }

    /**
     * @param auth the auth to set
     */
    public void setAuth(String auth) {
        this.auth = auth;
    }

    /**
     * @return the encryption
     */
    public String getEncryption() {
        return encryption;
    }

    /**
     * @param encryption the encryption to set
     */
    public void setEncryption(String encryption) {
        this.encryption = encryption;
    }

    /**
     * @return the BSSIDS
     */
    public List<BSSID> getBSSIDS() {
        return BSSIDS;
    }

    /**
     * @param BSSIDS the BSSIDS to set
     */
    public void setBSSIDS(List<BSSID> BSSIDS) {
        this.BSSIDS = BSSIDS;
    }
}
